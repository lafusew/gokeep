// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "gokeep",
	Short: "A simple password manager",
	Long: `gokeep is a simple password manager.
	
	It allows you to safely store your passwords locally on your machine.
	You can store and retrieve creds. 
	It can also help you an generate a safe password that would be automatically saved.

	Everything is encrypted and your master password is the encryption key.
	`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

